package edu.cs.hrbnu.model;

public class Weight {
	double studentWeight;
	double teacherWeight;
	double leaderWeight;
	double myselfWeight;
	public double getStudentWeight() {
		return studentWeight;
	}
	public void setStudentWeight(double studentWeight) {
		this.studentWeight = studentWeight;
	}
	public double getTeacherWeight() {
		return teacherWeight;
	}
	public void setTeacherWeight(double teacherWeight) {
		this.teacherWeight = teacherWeight;
	}
	public double getLeaderWeight() {
		return leaderWeight;
	}
	public void setLeaderWeight(double leaderWeight) {
		this.leaderWeight = leaderWeight;
	}
	public double getMyselfWeight() {
		return myselfWeight;
	}
	public void setMyselfWeight(double myselfWeight) {
		this.myselfWeight = myselfWeight;
	}
}
